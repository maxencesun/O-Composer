const TRANSLATIONS = Object.freeze({
  en: Object.freeze({}),
  zh: ZH
});

export function getLanguage() {
  const saved = localStorage.getItem(LANGUAGE_KEY);
  return TRANSLATIONS[saved] ? saved : "en";
}

export function setLanguage(language) {
  localStorage.setItem(LANGUAGE_KEY, TRANSLATIONS[language] ? language : "en");
}

export function t(key, replacements = {}, language = getLanguage()) {
  const value = String(TRANSLATIONS[language]?.[key] || key);
  return Object.entries(replacements).reduce(
    (text, [name, replacement]) => text.replace(new RegExp(`\\{${name}\\}`, "g"), String(replacement ?? "")),
    value
  );
}

export function optionLabel(value) {
  return t(String(value || ""));
}
